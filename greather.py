a=eval(input("Enter the value of A:- "))
b=eval(input("Enter the value of B:- "))
c=eval(input("Enter the value of C:- "))
if a>b and a>c:
    print("A is the greatest of all input number")
elif b>a and b>c:
    print("B is the greatest of all input number")
elif c>a and c>b:
    print("C is the greatest of all input number")
    
